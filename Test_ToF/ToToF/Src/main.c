static int Init_ToF(ToF_Handler *htof, uint8_t i2c_addr){
  HAL_GPIO_WritePin(TOF_RESET_DATA_GPIO_Port, TOF_RESET_DATA_Pin, GPIO_PIN_SET);

  HAL_GPIO_WritePin(TOF_RESET_SHIFT_GPIO_Port, TOF_RESET_SHIFT_Pin, GPIO_PIN_RESET);
  HAL_Delay(1);
  HAL_GPIO_WritePin(TOF_RESET_SHIFT_GPIO_Port, TOF_RESET_SHIFT_Pin, GPIO_PIN_SET);
  HAL_Delay(1);
  HAL_GPIO_WritePin(TOF_RESET_SHIFT_GPIO_Port, TOF_RESET_SHIFT_Pin, GPIO_PIN_RESET);

  //Wait for the chip to star
  HAL_Delay(TOF_DELAY);

  //Init structure
  ToF_Init_Struct(htof, &hi2c1);

  ToF_Params params;

  //Check the initial address
  if(ToF_Poke(&htof->dev) == -1)
    return -1;

  //Set the new address
  if(ToF_Set_Address(&htof->dev, i2c_addr) == -1)
    return -1;

  //Init the device
  if(ToF_Init_Device(&htof->dev) == -1)
    return -1;

  //Long range config
  TOF_LONG_RANGE_CONFIG(params);

  //Configure the device
  if(ToF_Configure_Device(&htof->dev, &params, VL53L0X_DEVICEMODE_CONTINUOUS_RANGING)== -1)
    return -1;

  //Start the device
  if(VL53L0X_StartMeasurement(&htof->dev) == -1)
    return -1;

  return 0;
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim) {
    if (htim->Instance == htim3.Instance) {
        int init = ToF_Poll_Measurement_Data(&tof[0]);
        int d = ToF_Get_Last_Range(&tof[0]);

        //Clignotement de la LED en dessous du seuil
        if (d < threshold && d != 0) {
            HAL_GPIO_TogglePin(LED_Status_GPIO_Port, LED_Status_Pin);
        }
    }
}
